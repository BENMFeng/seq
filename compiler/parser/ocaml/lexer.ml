              let n =
                if i < (l - 3) then
                try Some (int_of_string ("0x" ^ (String.sub s (i + 2) 2)))
                with Failure _ -> None
                else None
              in
              (match n with
              | Some n -> 3, Char.chr n
              | None -> raise (Ast.SyntaxError ("Invalid \\x escape", pos)))
            | c when is_octal c ->
              let n =
                if i < (l - 3) && (is_octal s.[i + 2]) && (is_octal s.[i + 3]) then 3
                else if i < (l - 2) && (is_octal s.[i + 2]) then 2
                else 1
